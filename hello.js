function peremeshat(massiv) {
    const novyi = [...massiv];
    for (let i = novyi.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [novyi[i], novyi[j]] = [novyi[j], novyi[i]];
    }
    return novyi;
}

function sozdat_igrokov(kolichestvo) {
    const igroki = [];
    for (let i = 0; i < kolichestvo; i++) {
        igroki.push({
            nomer: i + 1,
            nik: niki[i],
            rol: '',
            bally: 0,
            zhiv: true
        });
    }
    return igroki;
}

function razdat_roli(igroki, sostav) {
    const peremeshannye = peremeshat(sostav);
    for (let i = 0; i < igroki.length; i++) {
        igroki[i].rol = peremeshannye[i];
    }
}


